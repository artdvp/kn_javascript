let words = ["cockapoo", "beefalo"];

let moreWords = ["pegacorn", "schnoodle"];

let allWeirdWords = words.concat(moreWords);

console.log(allWeirdWords);
// [ 'cockapoo', 'beefalo', 'pegacorn', 'schnoodle' ]