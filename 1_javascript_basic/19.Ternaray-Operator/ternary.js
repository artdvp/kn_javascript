/**
 * Ternary Operator in JavaScript
 * https://alligator.io/js/ternary-operator/
 */
let isEven = true;

isEven ? console.log(2) : console.log(1);

// 2

/**
* Syntax
* 

condition ? firstExpression : secondExpression;

*/

let pokemon = 151;

let title = pokemon < 152 ? "noob" : "master";

console.log(title);
// noob
